    private void desabilitarCambios() {
        txtCantidadPla.setEnabled(false);
        txtCedula.setEnabled(false);
        txtContrasenna.setEnabled(false);
        txtNit.setEnabled(false);
        txtNombreAdmin.setEnabled(false);
        txtNombreEmpresa.setEnabled(false);
        txtUbicacion.setEnabled(false);
        txtUsername.setEnabled(false);
        txtValorCas.setEnabled(false);
        jcFechaNacimiento.setEnabled(false);
        btnGuardar.setEnabled(false);
        btnGuardar.setVisible(false);
    }

    private void cargarInfo(){
        if(caseta.getEstado().equals(Caseta.OCUPADO)){
            txtCantidadPla.setText(caseta.getSlots() + "");
            txtCedula.setText(caseta.getEmpresa().getAdmin().getCedula());
            txtContrasenna.setText(caseta.getEmpresa().getAdmin().getContrasenna());
            txtNit.setText(caseta.getEmpresa().getNIT() + "");
            txtNombreAdmin.setText(caseta.getEmpresa().getAdmin().getNombre());
            txtNombreEmpresa.setText(caseta.getEmpresa().getNombreEmpresa());
            txtUbicacion.setText(caseta.getCiudadOrigen());
            txtUsername.setText(caseta.getEmpresa().getAdmin().getUserName());
            txtValorCas.setText(caseta.getPrecio() + "");
            jcFechaNacimiento.setDate(DateTransformer.
                    LocalDateTimeToDate(caseta.getEmpresa().getAdmin().getFecha_nacimiento()));
            desabilitarCambios();
        }
    }

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            String nombreUsuario = txtUsername.getText();
            String nombre = txtNombreAdmin.getText();
            String contrasenna = txtContrasenna.getText();
            String cedula = txtCedula.getText();
            LocalDateTime fecha_nacimiento = DateTransformer.DateToLocalDateTime(jcFechaNacimiento.getDate());

            AdminFlota admins = new AdminFlota(nombre, cedula, fecha_nacimiento, nombreUsuario, contrasenna, Usuario.ADMIN_FLOTA);

            String nombreEmpresa = txtNombreEmpresa.getText();
            int NIT = Integer.parseInt(txtNit.getText());

            Empresa empresa = new Empresa(admins, nombreEmpresa, NIT);

            String ubicacion = txtUbicacion.getText();
            double precio = Double.parseDouble(txtValorCas.getText());
            int cantidadPlacetas = Integer.parseInt(txtCantidadPla.getText());
            controller.guardarEmpresa(empresa, NIT, precio, ubicacion);
            JOptionPane.showMessageDialog(null, "Empresa guardada con exito");
            cargarInfo();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


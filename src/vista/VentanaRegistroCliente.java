    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed

        try{
            String cedula = txtCedula.getText();
            String nombre = txtNombre.getText();
            String nombreUsuario = txtNombreUser.getText();
            String contrasenna = txtContrasena.getText();
            LocalDateTime fechaNacimiento = DateTransformer.DateToLocalDateTime(jdFechaNacimiento.getDate());
            
            Cliente cliente = new Cliente(nombre, cedula, fechaNacimiento, nombreUsuario, contrasenna, Usuario.CLIENTE);
            
            controller.validarUsuario(cliente);
            
            controller.guardarUsuario(cliente);
            
            this.dispose();
            
            new VentanaCliente(cliente).setVisible(true);
            
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistroActionPerformed

